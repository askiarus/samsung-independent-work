/*
Ответы на вопросы:
    4) Класс является "скелетом" для объектов. В объекте находятся все параметры, которые есть в классе (кроме конструкторов), но в них записаны значения
    5) Принципы ООП:
        * Инкапсуляция - возможность скрывать от пользователя и от частей кода данные в объекте
        * Наследование - возможность создавать классы, наследующие структуру других классов
        * Полиморфизм - возможность создания методов с одинаковым именем, но разными входными параметрами, т.е. использование одного метода для различных вычислений
    6) Модификаторы доступа:
        * private - есть доступ только внутри класса
        * protected - есть доступ внутри класса и из классов, наследующих его
        * default - есть доступ внутри поля видимсти класса и из классов, наследующих этот класс
        * public - есть доступ из любой "точки" программы
*/

